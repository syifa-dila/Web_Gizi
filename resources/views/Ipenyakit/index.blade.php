            <div class="mb-10 text-center">
                <h1 class="text-3xl font-bold text-gray-800">Informasi Penyakit Gizi Buruk pada Anak</h1>
                <p class="text-sm text-gray-500 mt-2">Berikut adalah penjelasan lengkap dari berbagai penyakit akibat gizi buruk yang umum terjadi pada anak-anak.</p>
            </div>

            <div class="mb-12">
                <h2 class="text-2xl font-semibold mb-2">1. Marasmus</h2>
                <p class="text-justify text-gray-700">
                <strong>Marasmus</strong> adalah salah satu bentuk kekurangan energi-protein paling berat yang disebabkan oleh kekurangan asupan kalori dan protein dalam waktu lama. Marasmus banyak dijumpai pada anak-anak balita di daerah miskin, terutama saat tidak cukup mendapat ASI atau makanan pendamping ASI yang bergizi. Tubuh anak menjadi sangat kurus, tampak tulang-tulang menonjol, dan kehilangan lapisan lemak tubuh.
                <br><br>
                <strong>Gejala:</strong> Berat badan sangat rendah (60% dari berat ideal), otot mengecil, wajah seperti orang tua, kulit kering dan keriput, lesu, tidak aktif, sering infeksi.
                <br><br>
                <strong>Dampak:</strong> Gangguan pertumbuhan, penurunan kekebalan tubuh, dan risiko kematian tinggi jika tidak segera ditangani.
                </p>
            </div>

            <div class="mb-12">
                <h2 class="text-2xl font-semibold mb-2">2. Kwashiorkor</h2>
                <p class="text-justify text-gray-700">
                <strong>Kwashiorkor</strong> adalah bentuk kekurangan gizi akibat kekurangan protein, meskipun asupan kalori masih mencukupi. Biasanya menyerang anak-anak yang berhenti menyusu terlalu dini. Tubuh tidak dapat mempertahankan keseimbangan cairan, menyebabkan edema (pembengkakan).
                <br><br>
                <strong>Gejala:</strong> Pembengkakan tubuh (terutama di kaki dan wajah), rambut kemerahan dan mudah rontok, kulit pecah-pecah, perut membuncit, nafsu makan rendah, perubahan mental seperti mudah marah atau apatis.
                <br><br>
                <strong>Dampak:</strong> Gangguan sistem imun, pertumbuhan terhambat, kerusakan organ (terutama hati), dan kematian bila tidak tertangani.
                </p>

            </div>

            <div class="mb-12">
                <h2 class="text-2xl font-semibold mb-2">3. Stunting</h2>
                <p class="text-justify text-gray-700">
                <strong>Stunting</strong> adalah kondisi pertumbuhan anak yang terhambat karena kekurangan gizi kronis sejak masa kandungan hingga usia 2 tahun. Stunting sering tidak disadari karena anak terlihat sehat, namun tinggi badannya jauh di bawah rata-rata.
                <br><br>
                <strong>Gejala:</strong> Tinggi badan lebih pendek dari standar usianya, pertumbuhan tulang lambat, perkembangan otak terhambat.
                <br><br>
                <strong>Dampak:</strong> Penurunan kecerdasan, risiko penyakit tidak menular di masa depan, dan produktivitas rendah saat dewasa.
                </p>
            </div>

            <div class="mb-12">
                <h2 class="text-2xl font-semibold mb-2">4. Wasting</h2>
                <p class="text-justify text-gray-700">
                <strong>Wasting</strong> adalah bentuk malnutrisi akut yang ditandai dengan penurunan berat badan secara drastis. Penyebabnya bisa karena infeksi berat, diare berkepanjangan, atau kekurangan makanan dalam waktu singkat.
                <br><br>
                <strong>Gejala:</strong> Berat badan sangat rendah dibandingkan tinggi badan, tubuh sangat kurus, tidak aktif, mata cekung, dehidrasi.
                <br><br>
                <strong>Dampak:</strong> Risiko kematian tinggi, infeksi berulang, dan gangguan organ vital.
                </p>
            </div>

            <div class="mb-12">
                <h2 class="text-2xl font-semibold mb-2">5. Kekurangan Energi Protein (KEP)</h2>
                <p class="text-justify text-gray-700">
                <strong>KEP</strong> adalah kondisi kekurangan asupan kalori dan protein secara bersamaan. KEP adalah istilah umum yang mencakup Marasmus dan Kwashiorkor. Biasanya terjadi karena kemiskinan, ketidaktahuan gizi, atau bencana alam.
                <br><br>
                <strong>Gejala:</strong> Anak tampak kurus, rambut tipis dan mudah rontok, lesu, nafsu makan rendah, infeksi berulang.
                <br><br>
                <strong>Dampak:</strong> Penurunan imunitas, perkembangan otak terganggu, berat badan dan tinggi badan tidak sesuai usia.
                </p>
            </div>

            <div class="mb-12">
                <h2 class="text-2xl font-semibold mb-2">6. Anemia Gizi Besi</h2>
                <p class="text-justify text-gray-700">
                <strong>Anemia Gizi Besi</strong> adalah jenis anemia yang paling umum, disebabkan oleh kekurangan zat besi. Besi penting untuk membentuk hemoglobin dalam darah. Kondisi ini sering dialami anak-anak karena pertumbuhan pesat, infeksi cacing, atau pola makan rendah zat besi.
                <br><br>
                <strong>Gejala:</strong> Wajah pucat, mudah lelah, pusing, jantung berdebar, sulit konsentrasi, kuku rapuh.
                <br><br>
                <strong>Dampak:</strong> Penurunan kemampuan belajar, risiko infeksi tinggi, gangguan tumbuh kembang.
                </p>
            </div>

            <div class="mb-12">
                <h2 class="text-2xl font-semibold mb-2">7. Hipovitaminosis</h2>
                <p class="text-justify text-gray-700">
                <strong>Hipovitaminosis</strong> adalah kondisi kekurangan satu atau lebih vitamin penting yang dibutuhkan tubuh, seperti vitamin A, D, B1, dan B12.
                <br><br>
                <ul class="list-disc ml-5 mt-2 text-gray-700">
                    <li><strong>Vitamin A:</strong> Rabun senja, mata kering, kebutaan.</li>
                    <li><strong>Vitamin D:</strong> Tulang bengkok (rakitis), nyeri tulang, pertumbuhan tulang terganggu.</li>
                    <li><strong>Vitamin B1:</strong> Beri-beri (kelemahan otot, gangguan jantung dan saraf).</li>
                    <li><strong>Vitamin B12:</strong> Anemia megaloblastik, kesemutan, gangguan saraf.</li>
                </ul>
                <br>
                <strong>Dampak:</strong> Kebutaan, kerusakan tulang, gangguan saraf, anemia berat.
                </p>
            </div>

            <div class="mb-12">
                <h2 class="text-2xl font-semibold mb-2">8. GAKY (Gangguan Akibat Kekurangan Yodium)</h2>
                <p class="text-justify text-gray-700">
                <strong>GAKY</strong> merupakan gangguan akibat kekurangan yodium, yaitu zat penting untuk produksi hormon tiroid. Banyak ditemukan di daerah pegunungan yang minim garam beryodium.
                <br><br>
                <strong>Gejala:</strong> Gondok (leher membesar), perkembangan otak terganggu, bicara lambat, tuli, kretinisme.
                <br><br>
                <strong>Dampak:</strong> Keterbelakangan mental dan fisik, IQ rendah, gangguan belajar, gangguan pertumbuhan.
                </p>
            </div>

            <div class="mb-12">
                <h2 class="text-2xl font-semibold mb-2">9. Kekurangan Zinc</h2>
                <p class="text-justify text-gray-700">
                <strong>Kekurangan Zinc</strong> dapat mengganggu pertumbuhan anak, penyembuhan luka, serta respon imun. Zinc banyak ditemukan dalam daging merah, telur, dan susu.
                <br><br>
                <strong>Gejala:</strong> Luka sulit sembuh, diare berulang, pertumbuhan terhambat, kerontokan rambut, nafsu makan menurun.
                <br><br>
                <strong>Dampak:</strong> Gangguan pertumbuhan, kerusakan jaringan tubuh, risiko infeksi tinggi.
                </p>
            </div>

            <div class="mb-12">
                <h2 class="text-2xl font-semibold mb-2">10. Obesitas Anak</h2>
                <p class="text-justify text-gray-700">
                <strong>Obesitas Anak</strong> terjadi ketika anak memiliki kelebihan berat badan dan lemak tubuh karena pola makan tinggi kalori dan rendah aktivitas fisik. Ini bukan hanya masalah penampilan, tapi juga kesehatan serius.
                <br><br>
                <strong>Gejala:</strong> Lemak berlebih di perut, cepat lelah, sesak napas saat beraktivitas, lipatan kulit basah dan iritasi.
                <br><br>
                <strong>Dampak:</strong> Diabetes tipe 2, hipertensi, gangguan jantung, dan masalah psikologis seperti rendah diri atau depresi.
                </p>
            </div>

            <div class="mb-12">
                <h2 class="text-2xl font-semibold mb-2">11. Hipervitaminosis</h2>
                <p class="text-justify text-gray-700">
                <strong>Hipervitaminosis</strong> adalah kelebihan konsumsi vitamin, terutama vitamin yang larut dalam lemak (A, D, E, K). Biasanya disebabkan oleh penggunaan suplemen berlebihan tanpa pengawasan dokter.
                <br><br>
                <strong>Gejala:</strong> Mual, muntah, nyeri otot dan tulang, kulit kering, mudah marah, gangguan penglihatan.
                <br><br>
                <strong>Dampak:</strong> Kerusakan hati, ginjal, tulang, serta gangguan neurologis jika dibiarkan.
                </p>
            </div>
        </div>
    </div>
